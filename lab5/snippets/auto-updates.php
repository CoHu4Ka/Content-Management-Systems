<?php
/**
 * ============================================================
 *  auto-updates.php — Автоматические обновления WordPress
 *  Лабораторная работа №5 · Безопасность WordPress
 *
 *  СПОСОБ 1: Добавить в wp-config.php (через константы)
 *  СПОСОБ 2: Добавить в functions.php активной темы (через фильтры)
 * ============================================================
 */

// ── СПОСОБ 1: константы в wp-config.php ─────────────────────

// Автообновление ядра: 'minor' — только патчи (рекомендуется)
//                      true    — все версии включая мажорные
//                      false   — отключить
define( 'WP_AUTO_UPDATE_CORE', 'minor' );


// ── СПОСОБ 2: фильтры в functions.php ───────────────────────

// Автообновление всех плагинов
add_filter( 'auto_update_plugin', '__return_true' );

// Автообновление всех тем
add_filter( 'auto_update_theme', '__return_true' );

// Автообновление переводов
add_filter( 'auto_update_translation', '__return_true' );

